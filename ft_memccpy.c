void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		if (src[i] == c)
			return (++dest);
		i++;
	}
	return (NULL);
}
