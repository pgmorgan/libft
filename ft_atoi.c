int		ft_atoi(const char *str)
{
	int nb;
	int sign;

	nb = 0;
	sign = 1;
	while (*str == ' ' || (*str > 8 && *str < 14))
		str++;
	if (*str == '+' || *str == '-')
		sign = (*str++ == '-' ? -1 : 1);
	while (*str > '0' && *str < '9')
		nb = (nb * 10) + ((*str++) - '0');
	return (nb * sign);
}
